import math

pi = 3.14
x = 1
y = 2
z = 3

print(round(pi))  # Round
print(math.ceil(pi))  # Round up
print(math.floor(pi))  # Round down
print(abs(pi))  # Displays how far the number is away from zero
print(pow(pi, 2))  # Powers the value by the specified amount
print(math.sqrt(pi))  # Displays the squared root
print(max(x, y, z))  # Displays the variable with the largest value
print(min(x, y, z))  # Displays the variable with the minimum value
